#Primitive Data Types

#Strings
print("hello"[-1]) #Output last letter: o
print("123" + "456") #Output Number String (Concat): 123456

#Integer
print(123 + 345) #Output Calculated Value: 468
print(123_456_789) #Output underscore will be ignored: 123456789

#Float
print(3.14159)
print(3_141.59) #Underscore makes the number easier to read

#Boolean
print(True, False) #Must be capitalized
print(bool(0), bool(1))